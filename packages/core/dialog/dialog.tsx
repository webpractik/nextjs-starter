'use client'

import { Dialog as DialogPrimitive } from '@base-ui/react/dialog'
import { XIcon } from 'lucide-react'

import * as React from 'react'
import { Button } from '../button'
import { cn } from '../cn'

export function Dialog({ ...props }: DialogPrimitive.Root.Props) {
	return <DialogPrimitive.Root data-slot="dialog" {...props} />
}

export function DialogTrigger({ ...props }: DialogPrimitive.Trigger.Props) {
	return <DialogPrimitive.Trigger data-slot="dialog-trigger" {...props} />
}

export function DialogPortal({ ...props }: DialogPrimitive.Portal.Props) {
	return <DialogPrimitive.Portal data-slot="dialog-portal" {...props} />
}

export function DialogClose({ ...props }: DialogPrimitive.Close.Props) {
	return <DialogPrimitive.Close data-slot="dialog-close" {...props} />
}

export function DialogOverlay({
	className,
	...props
}: DialogPrimitive.Backdrop.Props) {
	return (
		<DialogPrimitive.Backdrop
			data-slot="dialog-overlay"
			className={cn(`
     fixed inset-0 isolate z-50 bg-black/10 duration-100
     data-closed:animate-out data-closed:fade-out-0
     data-open:animate-in data-open:fade-in-0
     supports-backdrop-filter:backdrop-blur-xs
   `, className)}
			{...props}
		/>
	)
}

export function DialogContent({
	className,
	children,
	showCloseButton = true,
	...props
}: DialogPrimitive.Popup.Props & {
	showCloseButton?: boolean
}) {
	return (
		<DialogPortal>
			<DialogOverlay />
			<DialogPrimitive.Popup
				data-slot="dialog-content"
				className={cn(
					`
       fixed top-1/2 left-1/2 z-50 grid w-full max-w-[calc(100%-2rem)]
       -translate-1/2 gap-6 rounded-xl bg-background p-6 text-sm ring-1
       ring-foreground/10 duration-100 outline-none
       data-closed:animate-out data-closed:fade-out-0 data-closed:zoom-out-95
       data-open:animate-in data-open:fade-in-0 data-open:zoom-in-95
     `,
					className,
				)}
				{...props}
			>
				{children}
				{showCloseButton && (
					<DialogPrimitive.Close
						data-slot="dialog-close"
						render={(
							<Button
								variant="ghost"
								className="absolute top-4 right-4"
								size="icon-sm"
							/>
						)}
					>
						<XIcon />
						<span className="sr-only">Close</span>
					</DialogPrimitive.Close>
				)}
			</DialogPrimitive.Popup>
		</DialogPortal>
	)
}

export function DialogHeader({ className, ...props }: React.ComponentProps<'div'>) {
	return (
		<div
			data-slot="dialog-header"
			className={cn('flex flex-col gap-2', className)}
			{...props}
		/>
	)
}

export function DialogFooter({
	className,
	showCloseButton = false,
	children,
	...props
}: React.ComponentProps<'div'> & {
	showCloseButton?: boolean
}) {
	return (
		<div
			data-slot="dialog-footer"
			className={cn(
				`
      flex flex-col-reverse gap-2
      sm:flex-row sm:justify-end
    `,
				className,
			)}
			{...props}
		>
			{children}
			{showCloseButton && (
				<DialogPrimitive.Close render={<Button variant="outline" />}>
					Close
				</DialogPrimitive.Close>
			)}
		</div>
	)
}

export function DialogTitle({ className, ...props }: DialogPrimitive.Title.Props) {
	return (
		<DialogPrimitive.Title
			data-slot="dialog-title"
			className={cn('leading-none font-medium', className)}
			{...props}
		/>
	)
}

export function DialogDescription({
	className,
	...props
}: DialogPrimitive.Description.Props) {
	return (
		<DialogPrimitive.Description
			data-slot="dialog-description"
			className={cn(`
     text-sm text-muted-foreground
     *:[a]:underline *:[a]:underline-offset-3
     *:[a]:hover:text-foreground
   `, className)}
			{...props}
		/>
	)
}
